    private void foodTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_foodTableMouseClicked
        // TODO add your handling code here:

        int index = foodTable.getSelectedRow();
        if (index == -1) {

        } else {

            DichVu dv = dvList.get(index);

            String imgUrl = dv.getHinhanh();
            ImageIcon image = new ImageIcon(imgUrl);
            Image temp = image.getImage();
            ImageIcon scaledImage = new ImageIcon(temp.getScaledInstance(foodImageLabel.getWidth(),
                    foodImageLabel.getHeight(), Image.SCALE_SMOOTH));
            foodImageLabel.setIcon(scaledImage);
        }
    }//GEN-LAST:event_foodTableMouseClicked

    private void addServiceOrderbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addServiceOrderbtnActionPerformed
        // TODO add your handling code here:
        int index = foodTable.getSelectedRow();
        boolean hopLe = true;

        if (index == -1) {

        } else {

            DichVu dvItem = dvList.get(index);
            int qty = (int) qtySpn.getValue();
            if (qty == 0) {
                qtyWrong.setForeground(Color.red);
                qtyWrong.setText("Please choose quantity! :D");
                hopLe = false;
            }

            if (hopLe) {
                adminGUI.addServiceOrder(mapt, dvItem, qty, flag);
                qtyWrong.setForeground(new Color(5, 151, 32));
                qtySpn.setValue(0);
                qtyWrong.setText("Add service successfully! :D");
                //this.dispose();
            }
        }
    }//GEN-LAST:event_addServiceOrderbtnActionPerformed


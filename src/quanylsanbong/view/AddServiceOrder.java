    public void showFoodList(ArrayList<DichVu> dvList) {
        for (DichVu dv : dvList) {
            foodTableModel.addRow(new Object[]{dv.getMadv(), dv.getTendv(),
                dv.getDvt(), dv.getDongia()});
        }
    }

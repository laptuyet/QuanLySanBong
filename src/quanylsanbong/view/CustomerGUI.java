    public boolean isInWorkTime(String currTime, ArrayList<HeSo> rateList) {
        for (HeSo item : rateList) {
            if (cal.isTimeBetween(item.getTg_bd(), currTime, item.getTg_kt())) {
                return true;
            }
        }
        return false;
    }

